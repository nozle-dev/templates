import fs from 'fs-extra'
import path from 'path'

export async function generateFeatureGates(
  destination: string,
  featureCodes: string[]
): Promise<void> {
  if (featureCodes.length === 0) return

  const libPath = path.join(destination, 'lib')
  await fs.ensureDir(libPath)

  // Generate lib/feature-gates.ts
  const featureGatesContent = `// Auto-generated by create-nozle-app
// Feature gates based on environment variables

export const FEATURES = {
${featureCodes.map((code) => `  ${code}: process.env.NEXT_PUBLIC_FEATURE_${code.toUpperCase()} === 'true',`).join('\n')}
} as const

export type FeatureCode = keyof typeof FEATURES

export function hasFeature(feature: FeatureCode): boolean {
  return FEATURES[feature]
}

export function getEnabledFeatures(): FeatureCode[] {
  return Object.entries(FEATURES)
    .filter(([_, enabled]) => enabled)
    .map(([code]) => code as FeatureCode)
}
`

  await fs.writeFile(
    path.join(libPath, 'feature-gates.ts'),
    featureGatesContent,
    'utf-8'
  )
}

