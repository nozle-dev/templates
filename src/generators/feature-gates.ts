export function generateFeatureEnvVars(featureCodes: string[]): Record<string, boolean> {
  const features: Record<string, boolean> = {}

  // Default all features to false - user can enable them based on their plan
  for (const code of featureCodes) {
    features[code] = false
  }

  return features
}
